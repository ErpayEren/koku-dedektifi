-- RLS hardening:
-- Service role key (backend) bypasses RLS, but anon/authenticated traffic is blocked by default.
alter table public.app_users enable row level security;
alter table public.scent_wardrobe enable row level security;
alter table public.community_feed enable row level security;

do $$
begin
  if not exists (
    select 1
    from pg_policies
    where schemaname = 'public'
      and tablename = 'app_users'
      and policyname = 'app_users_owner_read'
  ) then
    create policy app_users_owner_read on public.app_users
      for select
      to authenticated
      using (id = auth.uid()::text);
  end if;
exception
  when duplicate_object then null;
end $$;

