function cleanString(value) {
  return typeof value === 'string' ? value.trim() : '';
}

function toList(value) {
  if (Array.isArray(value)) {
    return value.map((item) => cleanString(item)).filter(Boolean);
  }
  const single = cleanString(value);
  return single ? [single] : [];
}

const NOTE_MOLECULE_HINTS = [
  { keywords: ['bergamot', 'citrus', 'lemon', 'mandarin', 'orange', 'grapefruit'], molecules: ['Limonene', 'Citral'] },
  { keywords: ['lavender'], molecules: ['Linalool', 'Linalyl Acetate'] },
  { keywords: ['jasmine'], molecules: ['Hedione', 'Indole'] },
  { keywords: ['rose'], molecules: ['Phenylethanol', 'Damascenone'] },
  { keywords: ['patchouli'], molecules: ['Patchouli Alcohol'] },
  { keywords: ['vanilla'], molecules: ['Vanillin', 'Ethyl Vanillin'] },
  { keywords: ['amber', 'ambroxan', 'ambergris'], molecules: ['Ambroxide'] },
  { keywords: ['saffron'], molecules: ['Safranal'] },
  { keywords: ['pineapple'], molecules: ['Nootkatone'] },
  { keywords: ['apple'], molecules: ['Hexenol'] },
  { keywords: ['cedar', 'sandal', 'vetiver'], molecules: ['Cedrol'] },
  { keywords: ['musk'], molecules: ['Galaxolide', 'Muscone'] },
  { keywords: ['marine', 'aquatic', 'ozonic'], molecules: ['Calone'] },
  { keywords: ['tonka'], molecules: ['Coumarin'] },
  { keywords: ['clove', 'spice', 'pepper'], molecules: ['Eugenol'] },
  { keywords: ['rum', 'boozy', 'cognac'], molecules: ['Eugenol'] },
];

function guessMoleculesFromPyramid(pyramid) {
  const notes = [...toList(pyramid?.top), ...toList(pyramid?.middle), ...toList(pyramid?.base)];
  const lowerNotes = notes.map((item) => cleanString(item).toLowerCase());
  const picked = [];
  const seen = new Set();

  for (const note of lowerNotes) {
    for (const hint of NOTE_MOLECULE_HINTS) {
      if (!hint.keywords.some((keyword) => note.includes(keyword))) continue;
      for (const molecule of hint.molecules) {
        const key = molecule.toLowerCase();
        if (seen.has(key)) continue;
        seen.add(key);
        picked.push(molecule);
        if (picked.length >= 4) {
          return picked.map((name) => ({ name, note: 'heart', evidence: 'Catalog seed note-molecule hint' }));
        }
      }
    }
  }

  return picked.map((name) => ({ name, note: 'heart', evidence: 'Catalog seed note-molecule hint' }));
}

function entry({
  canonicalName,
  aliases = [],
  family,
  season = [],
  occasion = 'Gunduz',
  top = [],
  middle = [],
  base = [],
  tags = [],
  priceBand = 'mid',
  sourceUrl = '',
}) {
  const normalizedName = cleanString(canonicalName);
  const normalizedAliases = toList(aliases);
  const sourceLabel = sourceUrl ? `${normalizedName} official` : `${normalizedName} catalog seed`;
  return {
    canonicalName: normalizedName,
    aliases: normalizedAliases,
    family: cleanString(family) || 'Aromatik',
    season: toList(season),
    occasion: cleanString(occasion) || 'Gunduz',
    pyramid: {
      top: toList(top),
      middle: toList(middle),
      base: toList(base),
    },
    representativeMolecules: guessMoleculesFromPyramid({
      top,
      middle,
      base,
    }),
    tags: toList(tags),
    priceBand: cleanString(priceBand) || 'mid',
    sources: sourceUrl
      ? [{ label: sourceLabel, url: sourceUrl, kind: 'official' }]
      : [{ label: sourceLabel, url: '', kind: 'catalog-seed' }],
  };
}

