create extension if not exists vector;

-- Auth users table (cross-device identity source for web + mobile wrapper)
create table if not exists public.app_users (
  id text primary key,
  email text not null unique,
  name text not null default '',
  salt text not null default '',
  password_hash text not null default '',
  profile_json jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  last_login_at timestamptz
);

create index if not exists app_users_email_idx
  on public.app_users (email);

create table if not exists public.scent_wardrobe (
  user_id text primary key,
  shelf_json jsonb not null default '{}'::jsonb,
  updated_at timestamptz not null default now()
);

create index if not exists scent_wardrobe_updated_at_idx
  on public.scent_wardrobe (updated_at desc);

create table if not exists public.community_feed (
  user_id text primary key,
  feed_json jsonb not null default '[]'::jsonb,
  updated_at timestamptz not null default now()
);

create index if not exists community_feed_updated_at_idx
  on public.community_feed (updated_at desc);

