-- Optional: advisor RAG document store
create table if not exists public.perfume_docs (
  id bigint generated by default as identity primary key,
  external_id text not null unique,
  doc_type text not null default 'perfume',
  title text not null,
  content text not null default '',
  family text not null default '',
  occasion text not null default '',
  url text not null default '',
  embedding vector(768) not null,
  created_at timestamptz not null default now()
);

alter table public.perfume_docs
  add column if not exists external_id text;
alter table public.perfume_docs
  add column if not exists doc_type text not null default 'perfume';

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'perfume_docs_external_id_key'
  ) then
    alter table public.perfume_docs
      add constraint perfume_docs_external_id_key unique (external_id);
  end if;
exception
  when duplicate_object then null;
end $$;

create index if not exists perfume_docs_embedding_idx
  on public.perfume_docs using ivfflat (embedding vector_cosine_ops)
  with (lists = 100);

create index if not exists perfume_docs_doc_type_idx
  on public.perfume_docs (doc_type);

create or replace function public.match_perfume_docs(
  query_embedding vector(768),
  match_count int default 6
)
returns table (
  id bigint,
  doc_type text,
  title text,
  content text,
  family text,
  occasion text,
  url text,
  score float
)
language sql
stable
as $$
  select
    d.id,
    d.doc_type,
    d.title,
    d.content,
    d.family,
    d.occasion,
    d.url,
    1 - (d.embedding <=> query_embedding) as score
  from public.perfume_docs d
  order by d.embedding <=> query_embedding
  limit greatest(match_count, 1);
$$;
